// Date

let myDate=new Date()
console.log(myDate)  // 2024-08-03T12:56:34.437Z
console.log(myDate.toDateString()) // Sat Aug 03 2024
console.log(myDate.toLocaleString()) // 3/8/2024, 6:26:34 pm
console.log(myDate.toString()) //Sat Aug 03 2024 18:26:34 GMT+0530 (India Standard Time)
console.log(myDate.getDate()) // 3
console.log(myDate.getMinutes())// 28